#include <stdio.h>

int main() {
    int a = 20, b = 5;

    printf("a = %d, b = %d\n", a, b);

    printf("Addition: %d + %d = %d\n", a, b, a + b);
    printf("Subtraction: %d - %d = %d\n", a, b, a - b);
    printf("Multiplication: %d * %d = %d\n", a, b, a * b);
    printf("Division: %d / %d = %d\n", a, b, a / b);

    return 0;
}
